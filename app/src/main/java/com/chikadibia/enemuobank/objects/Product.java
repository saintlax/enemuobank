package com.chikadibia.enemuobank.objects;

public class Product {
    public int id;
    public int icon;
    public String name;
    public String type;

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(String type) {
        this.type = type;
    }

}
